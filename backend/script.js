function convertToRAG(data) {
  const docs = [];

  data.forEach((chunk, index) => {
    const stt = index;

    if (chunk.type === "overview") {
      docs.push(`
TỔNG QUAN TUYỂN SINH PTIT 2025
${chunk.text}
Tổng số ngành/chương trình: ${chunk.metadata.totalPrograms}
Tổng chỉ tiêu tuyển sinh: ${chunk.metadata.totalChiTieu}
      `.trim());
      return;
    }

    const md = chunk.metadata;

    const toHop = md.toHopXetTuyen?.length
      ? md.toHopXetTuyen.join(", ")
      : "Không rõ";

    const doc = `Ngành: ${md.tenNganh}
- Năm tuyển sinh: 2025
- Mã xét tuyển: ${md.maXetTuyen}
- Mã ngành: ${md.maNganhNhom}
- Nhóm ngành: ${md.tenNganhNhom}
- Loại chương trình: ${md.category}
- Chỉ tiêu tuyển sinh: ${md.chiTieu} sinh viên
- Tổ hợp xét tuyển: ${toHop}
- Mô tả: ${chunk.text}
    `.trim();

    docs.push(doc);
  });

  return docs.join("\n");
}

